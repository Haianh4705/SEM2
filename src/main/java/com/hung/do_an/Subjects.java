package com.hung.do_an;

public class Subjects {
    String id;
    String name;

    public Subjects() {
    }

    public Subjects(String id, String name) {
        this.id = id;
        this.name = name;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}

